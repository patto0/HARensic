def normalize_url(raw: str) -> str:
    """
    Strip tracking parameters and chatgpt attribution suffixes.
    Returns a clean, canonical URL for deduplication.
    """
    if not raw or not raw.startswith("http"):
        return raw
    try:
        parsed = urllib.parse.urlparse(raw)
        qs = urllib.parse.parse_qs(parsed.query, keep_blank_values=True)
        # Remove known tracking params
        clean_qs = {k: v for k, v in qs.items() if k not in _TRACKING_PARAMS}
        new_query = urllib.parse.urlencode(clean_qs, doseq=True)
        clean = parsed._replace(query=new_query)
        return urllib.parse.urlunparse(clean)
    except Exception:
        return raw


def extract_domain(url: str) -> str:
    """Extract the registered domain from a URL."""
    try:
        return urllib.parse.urlparse(url).netloc.lower()
    except Exception:
        return ""


# ─────────────────────────────────────────────────────────────────────────────
#  2. SSE / EVENT-STREAM DEEP PARSER
# ─────────────────────────────────────────────────────────────────────────────

def _parse_sse_lines(text: str) -> List[Dict]:
    """
    Parse raw SSE text into a sequence of (seq, data_dict) events.
    Handles both newline-delimited and concatenated-chunk formats.
    """
    events = []
    seq = 0
    for line in text.splitlines():
        s = line.strip()
        if not s.startswith("data:"):
            continue
        raw = s[5:].strip()
        if not raw or raw == "[DONE]":
            continue
        try:
            parsed = json.loads(raw)
            events.append({"seq": seq, "data": parsed})
            seq += 1
        except (json.JSONDecodeError, ValueError):
            pass
    return events


def _walk_patches(v_field) -> List[Dict]:
    """
    Recursively collect all patch objects from the 'v' field of SSE deltas.
    Handles both dict (full message) and list (patch array) variants.
    """
    patches = []
    if isinstance(v_field, dict):
        patches.append(v_field)
    elif isinstance(v_field, list):
        for item in v_field:
            if isinstance(item, dict):
                patches.append(item)
    return patches


def extract_ai_urls_from_sse(sse_text: str, entry_url: str, entry_ts: str,
                               timeline: StreamTimeline) -> List[Dict]:
    """
    Deep-parse an SSE stream to extract all AI-attributed URLs.

    Extracts from:
      - search_result_groups       (raw search index results)
      - content_references patches (cited article content + snippets)
      - sources_footnote           (final citation list shown in UI)
      - url_moderation events      (AI-generated URLs being safety-checked)
      - search_model_queries       (the refined search query used)
      - tether_browsing_display    (Claude/legacy browsing results)

    Returns list of raw URL dicts with context fields.
    """
    results = []
    events = _parse_sse_lines(sse_text)

    for evt in events:
        seq  = evt["seq"]
        data = evt["data"]
        if not isinstance(data, dict):
            continue

        dtype = data.get("type", "")

        # ── message_marker: search_start ─────────────────────────────────
        if dtype == "message_marker":
            marker = data.get("marker", "")
            if marker == "search_start" and timeline.search_start_seq is None:
                timeline.search_start_seq = seq
                timeline.search_start_ts  = entry_ts

        # ── server_ste_metadata: tool name ───────────────────────────────
        if dtype == "server_ste_metadata":
            meta = data.get("metadata", {}) or {}
            if meta.get("tool_name"):
                timeline.tool_name = meta["tool_name"]
            if meta.get("tool_invoked") and timeline.tool_invoked_seq is None:
                timeline.tool_invoked_seq = seq

        # ── search_model_queries ──────────────────────────────────────────
        v = data.get("v", {})
        if isinstance(v, dict):
            msg   = v.get("message", {}) or {}
            meta  = msg.get("metadata", {}) or {}
            content = msg.get("content", {}) or {}
            ctype = content.get("content_type", "")

            # search_model_queries  → reveals refined search query
            smq = meta.get("search_model_queries")
            if smq and isinstance(smq, dict):
                for q in smq.get("queries", []):
                    if q and q not in timeline.search_queries:
                        timeline.search_queries.append(q)

            # search_result_groups → raw search index results
            srg = meta.get("search_result_groups")
            if srg and isinstance(srg, list):
                for group in srg:
                    domain = group.get("domain", "")
                    for entry in group.get("entries", []):
                        url = entry.get("url", "")
                        if url:
                            results.append({
                                "url":        url,
                                "url_role":   "search_result",
                                "source":     "search_result_groups",
                                "domain":     domain or extract_domain(url),
                                "title":      entry.get("title", ""),
                                "snippet":    entry.get("snippet", ""),
                                "pub_date":   "",
                                "result_src": entry.get("result_source", ""),
                                "seq":        seq,
                                "entry_url":  entry_url,
                                "entry_ts":   entry_ts,
                            })

            # content_references → cited articles with full snippets
            refs = meta.get("content_references")
            if refs and isinstance(refs, list):
                for ref in refs:
                    if not isinstance(ref, dict):
                        continue
                    ref_type = ref.get("type", "")

                    # sources_footnote → final citation list
                    if ref_type == "sources_footnote":
                        for src in ref.get("sources", []):
                            url = src.get("url", "")
                            if url:
                                results.append({
                                    "url":      url,
                                    "url_role": "cited_source",
                                    "source":   "sources_footnote",
                                    "domain":   src.get("attribution", extract_domain(url)),
                                    "title":    src.get("title", ""),
                                    "snippet":  "",
                                    "pub_date": "",
                                    "result_src": "",
                                    "seq":      seq,
                                    "entry_url": entry_url,
                                    "entry_ts":  entry_ts,
                                })

                    # grouped_webpages → full article content ChatGPT read
                    if ref_type == "grouped_webpages" or "items" in ref:
                        for item in ref.get("items", []):
                            url = item.get("url", "")
                            if url:
                                pub_raw = item.get("pub_date")
                                try:
                                    from datetime import datetime
                                    pub_str = datetime.utcfromtimestamp(pub_raw).strftime(
                                        "%Y-%m-%d") if pub_raw else ""
                                except Exception:
                                    pub_str = str(pub_raw) if pub_raw else ""
                                results.append({
                                    "url":      url,
                                    "url_role": "retrieved_content",
                                    "source":   "content_references_grouped_webpages",
                                    "domain":   item.get("attribution", extract_domain(url)),
                                    "title":    item.get("title", ""),
                                    "snippet":  item.get("snippet", "")[:400],
                                    "pub_date": pub_str,
                                    "result_src": item.get("result_source", ""),
                                    "seq":      seq,
                                    "entry_url": entry_url,
                                    "entry_ts":  entry_ts,
                                })
                            # Supporting websites within an item
                            for sw in item.get("supporting_websites", []):
                                sw_url = sw.get("url", "")
                                if sw_url:
                                    results.append({
                                        "url":      sw_url,
                                        "url_role": "supporting_source",
                                        "source":   "content_references_supporting_websites",
                                        "domain":   sw.get("attribution", extract_domain(sw_url)),
                                        "title":    sw.get("title", ""),
                                        "snippet":  "",
                                        "pub_date": "",
                                        "result_src": sw.get("result_source", ""),
                                        "seq":      seq,
                                        "entry_url": entry_url,
                                        "entry_ts":  entry_ts,
                                    })

            # safe_urls inside content_references patches
            for patch in _walk_patches(v):
                p_key = str(patch.get("p", ""))
                p_val = patch.get("v")
                if "content_references" in p_key and isinstance(p_val, list):
                    for url in p_val:
                        if isinstance(url, str) and url.startswith("http"):
                            results.append({
                                "url":      url,
                                "url_role": "citation_patch_url",
                                "source":   "content_references_patch",
                                "domain":   extract_domain(url),
                                "title":    "",
                                "snippet":  "",
                                "pub_date": "",
                                "result_src": "",
                                "seq":      seq,
                                "entry_url": entry_url,
                                "entry_ts":  entry_ts,
                            })

            # tether_browsing_display (Claude / legacy ChatGPT browsing)
            if ctype == "tether_browsing_display":
                for part in content.get("result", {}).get("parts", []):
                    if isinstance(part, dict):
                        url = part.get("url", "")
                        if url:
                            results.append({
                                "url":      url,
                                "url_role": "browsing_result",
                                "source":   "tether_browsing_display",
                                "domain":   extract_domain(url),
                                "title":    part.get("title", ""),
                                "snippet":  part.get("snippet", ""),
                                "pub_date": "",
                                "result_src": "tether",
                                "seq":      seq,
                                "entry_url": entry_url,
                                "entry_ts":  entry_ts,
                            })

            # code content_type: SonicBrowserTool / search() call
            if ctype == "code":
                code_text = content.get("text", "")
                urls_in_code = re.findall(r'https?://[^\s\'"<>]+', code_text)
                for url in urls_in_code:
                    results.append({
                        "url":      url,
                        "url_role": "tool_call_url",
                        "source":   "sse_code_block",
                        "domain":   extract_domain(url),
                        "title":    "",
                        "snippet":  "",
                        "pub_date": "",
                        "result_src": "",
                        "seq":      seq,
                        "entry_url": entry_url,
                        "entry_ts":  entry_ts,
                    })

        # ── Patch-array format (list v field) ─────────────────────────────
        if isinstance(v, list):
            for patch in v:
                if not isinstance(patch, dict):
                    continue
                pval = patch.get("v")
                pkey = str(patch.get("p", ""))

                # Inline safe_urls arrays
                if isinstance(pval, list):
                    for item in pval:
                        if isinstance(item, str) and item.startswith("http"):
                            role = "citation_patch_url"
                            if "search_result" in pkey:
                                role = "search_result"
                            results.append({
                                "url":      item,
                                "url_role": role,
                                "source":   f"patch:{pkey[:60]}",
                                "domain":   extract_domain(item),
                                "title":    "",
                                "snippet":  "",
                                "pub_date": "",
                                "result_src": "",
                                "seq":      seq,
                                "entry_url": entry_url,
                                "entry_ts":  entry_ts,
                            })

                # Patch value is a dict (grouped_webpages embedded in patch)
                if isinstance(pval, dict):
                    for item in pval.get("items", []):
                        if not isinstance(item, dict):
                            continue
                        url = item.get("url", "")
                        if url and url.startswith("http"):
                            pub_raw = item.get("pub_date")
                            try:
                                from datetime import datetime
                                pub_str = datetime.utcfromtimestamp(pub_raw).strftime(
                                    "%Y-%m-%d") if pub_raw else ""
                            except Exception:
                                pub_str = str(pub_raw) if pub_raw else ""
                            results.append({
                                "url":      url,
                                "url_role": "retrieved_content",
                                "source":   f"patch_items:{pkey[:40]}",
                                "domain":   item.get("attribution", extract_domain(url)),
                                "title":    item.get("title", ""),
                                "snippet":  item.get("snippet", "")[:400],
                                "pub_date": pub_str,
                                "result_src": item.get("result_source", ""),
                                "seq":      seq,
                                "entry_url": entry_url,
                                "entry_ts":  entry_ts,
                            })

    return results


