"""
URL Attribution Engine — Forensic HAR Analysis
===============================================
Extracts, classifies, and attributes every URL involved in an LLM workflow
to one of three forensic categories:

    HUMAN    — URLs initiated by direct user action (navigation, clicks)
    AI       — URLs accessed/generated by the model via tool use
    PLATFORM — URLs belonging to infrastructure (CDN, telemetry, APIs)

Attribution is temporal, contextual, metadata-aware, and stream-aware.
Every attribution includes a confidence score (0–100) and evidence text
suitable for DFRWS / academic forensic reporting.

Core forensic pipeline
----------------------
1. url_extractor        — Harvests raw URLs from all HAR layers
2. stream_url_parser    — Deep-parses SSE / event-stream chunks for AI URLs
3. url_normalizer       — Strips tracking params, deduplicates
4. forensic_attributor  — Deterministic heuristic classification
5. timeline_correlator  — Anchors attribution to stream event timestamps
6. confidence_scorer    — Produces 0–100 evidence-based confidence
"""

