@dataclass
class URLArtifact:
    """Single forensic URL record ready for table output."""
    raw_url:        str
    normalized_url: str
    domain:         str
    attribution:    str          # "HUMAN" | "AI" | "PLATFORM"
    confidence:     int          # 0–100
    evidence:       str          # Human-readable forensic rationale
    har_location:   str          # Where in the HAR this was found
    json_path:      str          # Precise JSON path
    source_type:    str          # "network_request" | "sse_stream" | "metadata"
    trigger:        str          # What triggered this URL (search_start, navigation, etc.)
    timeline_anchor: str         # ISO timestamp or stream event ref
    url_role:       str          # "search_result" | "cited_source" | "cdn_asset" | etc.
    snippet:        str          # Article snippet if available (AI URLs)
    pub_date:       str          # Publication date if available (AI URLs)

    # Canonical artifact fields for table output
    @property
    def artifact(self) -> str:
        return f"url_{self.url_role}"

    @property
    def value(self) -> str:
        return self.normalized_url

    @property
    def reason(self) -> str:
        return self.evidence

    def to_row(self) -> Dict:
        """Convert to standard artifact row dict for existing display/export."""
        return {
            "artifact":     self.artifact,
            "value":        self.normalized_url,
            "har_location": self.har_location,
            "json_path":    self.json_path,
            "attribution":  self.attribution,
            "reason":       self.evidence,
            # Extended URL-specific fields
            "url_raw":      self.raw_url,
            "url_domain":   self.domain,
            "url_confidence": self.confidence,
            "url_trigger":  self.trigger,
            "url_role":     self.url_role,
            "url_snippet":  self.snippet,
            "url_pub_date": self.pub_date,
            "url_timeline": self.timeline_anchor,
            "url_source_type": self.source_type,
        }


@dataclass
class StreamTimeline:
    """
    Records key temporal markers extracted from the SSE stream.
    Used to anchor URL attribution temporally (before/after search_start etc.)
    """
    search_start_seq:    Optional[int]   = None   # SSE seq# of search_start marker
    search_start_ts:     str             = ""
    tool_invoked_seq:    Optional[int]   = None   # SSE seq# of SonicBrowserTool event
    last_token_seq:      Optional[int]   = None
    first_token_seq:     Optional[int]   = None
    favicon_fetch_ts:    str             = ""     # When browser fetched favicons
    search_queries:      List[str]       = field(default_factory=list)
    tool_name:           str             = ""


