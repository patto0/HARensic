def classify_favicon_request(url: str, ts: str, timeline: StreamTimeline) -> URLArtifact:
    """
    Favicon fetches are a critical forensic bridge:
    - The domain in the favicon URL reveals which AI-cited sources were used
    - The timing (after last_token) confirms they're triggered by citation rendering
    - Attribution: PLATFORM (the platform's UI fetches them, not the AI or user)
    """
    # Extract the target domain from favicon URL query params
    cited_domain = ""
    for param in ["domain", "url"]:
        m = re.search(rf"[?&]{param}=([^&]+)", url)
        if m:
            cited_domain = urllib.parse.unquote(m.group(1))
            cited_domain = extract_domain(cited_domain) if cited_domain.startswith("http") else cited_domain
            break

    evidence = (
        f"PLATFORM: Favicon fetch request to Google's favicon CDN service. "
        f"The URL reveals that the AI cited a source from '{cited_domain}'. "
        f"These requests are made automatically by the ChatGPT frontend UI "
        f"to render the small site icons shown next to each citation link. "
        f"Timing after the SSE stream completes confirms this is UI rendering "
        f"triggered by AI-generated citations, not user navigation. "
        f"Attribution is PLATFORM because the platform's frontend code "
        f"initiates these requests, though they are forensic evidence of AI URL activity. "
        f"Confidence: 98%."
    )

    return URLArtifact(
        raw_url=url,
        normalized_url=normalize_url(url),
        domain=extract_domain(url),
        attribution="PLATFORM",
        confidence=98,
        evidence=evidence,
        har_location=f"GET {url[:80]}... (favicon CDN)",
        json_path="log.entries[].request.url (gstatic/google favicon)",
        source_type="network_request",
        trigger="citation_render",
        timeline_anchor=ts,
        url_role="favicon_cdn",
        snippet=f"Reveals AI cited domain: {cited_domain}",
        pub_date="",
    )


# ─────────────────────────────────────────────────────────────────────────────
#  6. MAIN ATTRIBUTION ENGINE
# ─────────────────────────────────────────────────────────────────────────────

def attribute_url(raw_url: str, context: Dict, timeline: StreamTimeline) -> URLArtifact:
    """
    Core deterministic heuristic classifier for a single URL.

    Decision order (short-circuit on first match):
      1. Empty / non-HTTP  → skip
      2. Favicon requests  → PLATFORM (with cited domain extraction)
      3. Known platform domain / path → PLATFORM
      4. AI source (from SSE stream metadata) → AI
      5. Network request before search_start → lean HUMAN
      6. Network request with navigation initiator → HUMAN
      7. Default → PLATFORM (conservative)
    """
    url      = raw_url.strip()
    domain   = extract_domain(url)
    source   = context.get("source", "")
    url_role = context.get("url_role", "unknown")
    ts       = context.get("entry_ts", context.get("ts", ""))
    seq      = context.get("seq")

    # ── Favicon ──────────────────────────────────────────────────────────
    if "faviconV2" in url or "/s2/favicons" in url:
        return classify_favicon_request(url, ts, timeline)

    # ── Platform domain check ─────────────────────────────────────────────
    is_plat_domain, plat_conf, plat_reason = _is_platform_domain(domain)
    is_plat_path,   path_conf, path_desc   = _classify_platform_path(url)

    if is_plat_domain or is_plat_path:
        confidence = max(plat_conf, path_conf)
        evidence   = _build_platform_evidence(
            url, domain, path_desc, plat_reason, confidence
        )
        path_part = _path(url)

        # Determine specific role
        if "sentinel" in url:
            role = "sentinel_endpoint"
        elif "cdn" in url or ".js" in path_part or ".css" in path_part or ".svg" in path_part:
            role = "cdn_asset"
        elif "telemetry" in url or "ces/v1" in url or "datadoghq" in url:
            role = "telemetry"
        elif "conversation" in url:
            role = "conversation_api"
        elif "favicon" in url:
            role = "favicon_cdn"
        elif "analytics" in url or "tagmanager" in url:
            role = "analytics"
        elif "auth" in url or "accounts" in url:
            role = "auth_endpoint"
        else:
            role = "platform_api"

        return URLArtifact(
            raw_url=raw_url,
            normalized_url=normalize_url(url),
            domain=domain,
            attribution="PLATFORM",
            confidence=confidence,
            evidence=evidence,
            har_location=context.get("har_location",
                         f"{context.get('method','GET')} {_path(url)} [{source}]"),
            json_path=context.get("json_path", f"log.entries[{context.get('entry_index','')}].request.url"),
            source_type=context.get("source_type", "network_request"),
            trigger="platform_infrastructure",
            timeline_anchor=ts,
            url_role=role,
            snippet="",
            pub_date="",
        )

    # ── AI source (from SSE stream) ───────────────────────────────────────
    if source in (
        "search_result_groups",
        "sources_footnote",
        "content_references_grouped_webpages",
        "content_references_supporting_websites",
        "content_references_patch",
        "tether_browsing_display",
        "sse_code_block",
    ) or source.startswith("patch"):

        # Confidence varies by source
        conf_map = {
            "search_result_groups":                  90,
            "sources_footnote":                      97,
            "content_references_grouped_webpages":   95,
            "content_references_supporting_websites": 88,
            "content_references_patch":              85,
            "tether_browsing_display":               95,
            "sse_code_block":                        92,
        }
        confidence = conf_map.get(source, 85)

        # Higher confidence if after search_start
        trigger = ""
        if (timeline.search_start_seq is not None and
                seq is not None and seq > timeline.search_start_seq):
            confidence = min(confidence + 3, 99)
            trigger = "search_start"

        evidence = _build_ai_evidence(
            url=url,
            domain=domain,
            url_role=url_role,
            source=source,
            trigger=trigger,
            tool_name=timeline.tool_name,
            search_queries=timeline.search_queries,
            snippet=context.get("snippet", ""),
            confidence=confidence,
        )

        return URLArtifact(
            raw_url=raw_url,
            normalized_url=normalize_url(url),
            domain=domain,
            attribution="AI",
            confidence=confidence,
            evidence=evidence,
            har_location=(
                f"SSE stream: {source} "
                f"(seq={seq}, entry={context.get('entry_url','')[:50]})"
            ),
            json_path=(
                f"SSE delta.v.message.metadata.{source}"
                if "patch" not in source else
                f"SSE delta.v[].p contains {source}"
            ),
            source_type="sse_stream",
            trigger=trigger or "tool_execution",
            timeline_anchor=ts,
            url_role=url_role,
            snippet=context.get("snippet", ""),
            pub_date=context.get("pub_date", ""),
        )

    # ── Network request — HUMAN vs PLATFORM disambiguation ────────────────
    initiator_type = context.get("initiator_type", "")
    resource_type  = context.get("resource_type", "")
    referer        = context.get("referer", "")
    method         = context.get("method", "GET")
    entry_index    = context.get("entry_index", 0)

    # Navigation-initiated top-level document → strong HUMAN signal
    if initiator_type == "navigation" or resource_type == "document":
        confidence = 80
        evidence   = _build_human_evidence(
            url, domain, initiator_type, resource_type, referer, confidence
        )
        return URLArtifact(
            raw_url=raw_url,
            normalized_url=normalize_url(url),
            domain=domain,
            attribution="HUMAN",
            confidence=confidence,
            evidence=evidence,
            har_location=f"[{entry_index}] {method} {_path(url)}",
            json_path=f"log.entries[{entry_index}].request.url",
            source_type="network_request",
            trigger="user_navigation",
            timeline_anchor=ts,
            url_role="user_navigation",
            snippet="",
            pub_date="",
        )

    # Default: conservative PLATFORM (most unexplained requests in a ChatGPT
    # session are platform-initiated scripts / XHR background calls)
    evidence = (
        f"PLATFORM (default): URL '{domain}' could not be definitively attributed "
        f"to user navigation or AI tool use. In a ChatGPT session, most background "
        f"XHR/fetch requests to non-third-party domains are platform-initiated. "
        f"Confidence: 60% (low — consider manual review)."
    )
    return URLArtifact(
        raw_url=raw_url,
        normalized_url=normalize_url(url),
        domain=domain,
        attribution="PLATFORM",
        confidence=60,
        evidence=evidence,
        har_location=f"[{entry_index}] {method} {_path(url)}",
        json_path=f"log.entries[{entry_index}].request.url",
        source_type="network_request",
        trigger="platform_background",
        timeline_anchor=ts,
        url_role="platform_background",
        snippet="",
        pub_date="",
    )


# ─────────────────────────────────────────────────────────────────────────────
#  7. DEDUPLICATION
# ─────────────────────────────────────────────────────────────────────────────

def deduplicate_artifacts(artifacts: List[URLArtifact]) -> List[URLArtifact]:
    """
    Deduplicate by normalized URL, keeping highest-confidence record.
    AI attribution always wins over PLATFORM for same URL.
    """
    seen: Dict[str, URLArtifact] = {}
    for a in artifacts:
        key = a.normalized_url
        if key not in seen:
            seen[key] = a
        else:
            existing = seen[key]
            # AI > HUMAN > PLATFORM in priority
            priority = {"AI": 3, "HUMAN": 2, "PLATFORM": 1}
            if priority.get(a.attribution, 0) > priority.get(existing.attribution, 0):
                seen[key] = a
            elif (priority.get(a.attribution, 0) == priority.get(existing.attribution, 0)
                  and a.confidence > existing.confidence):
                seen[key] = a
    return list(seen.values())


# ─────────────────────────────────────────────────────────────────────────────
#  8. RELATIONSHIP MAP BUILDER
# ─────────────────────────────────────────────────────────────────────────────

def build_url_relationship_map(artifacts: List[URLArtifact],
                                 timeline: StreamTimeline) -> List[Dict]:
    """
    Reconstructs the full retrieval chain:
      User Query → search_model_queries → search_result_groups
                → content_references (retrieved_content)
                → sources_footnote (cited_source)
                → favicon_cdn (UI rendering)

    Returns a list of relationship records for export.
    """
    chain = []

    # Layer 0: Search queries
    for q in timeline.search_queries:
        chain.append({
            "layer":       0,
            "layer_name":  "Search Query",
            "value":       q,
            "attribution": "AI",
            "description": "Refined query generated by AI and sent to search engine",
        })

    # Layer 1: Raw search results
    for a in artifacts:
        if a.url_role == "search_result":
            chain.append({
                "layer":       1,
                "layer_name":  "Search Result (Raw)",
                "value":       a.normalized_url,
                "domain":      a.domain,
                "attribution": "AI",
                "description": "Raw search index result returned to AI (may not be cited)",
            })

    # Layer 2: Retrieved content
    for a in artifacts:
        if a.url_role == "retrieved_content":
            chain.append({
                "layer":       2,
                "layer_name":  "Retrieved Content",
                "value":       a.normalized_url,
                "domain":      a.domain,
                "attribution": "AI",
                "description": f"Full page content read by AI: {a.title[:60] if hasattr(a,'title') else ''}",
            })

    # Layer 3: Cited sources
    for a in artifacts:
        if a.url_role == "cited_source":
            chain.append({
                "layer":       3,
                "layer_name":  "Cited Source (Final)",
                "value":       a.normalized_url,
                "domain":      a.domain,
                "attribution": "AI",
                "description": "Source cited in AI response visible to user",
            })

    # Layer 4: Favicon fetches (UI proof)
    for a in artifacts:
        if a.url_role == "favicon_cdn":
            chain.append({
                "layer":       4,
                "layer_name":  "Favicon Fetch (UI Proof)",
                "value":       a.normalized_url,
                "domain":      a.domain,
                "attribution": "PLATFORM",
                "description": f"Browser fetched favicon for cited domain. Evidence: {a.snippet}",
            })

    return chain


# ─────────────────────────────────────────────────────────────────────────────
#  9. TOP-LEVEL ORCHESTRATOR
# ─────────────────────────────────────────────────────────────────────────────

def run_url_attribution(entries: List[Dict]) -> Tuple[List[URLArtifact], StreamTimeline, List[Dict]]:
    """
    Full URL forensic attribution pipeline.

    Parameters
    ----------
    entries : HAR log entries

    Returns
    -------
    (url_artifacts, timeline, relationship_chain)
    """
    timeline   = StreamTimeline()
    raw_urls: List[Dict] = []

    # Pass 1: Extract AI URLs from SSE streams
    for entry in entries:
        content   = entry["response"]["content"]
        mime      = content.get("mimeType", "")
        entry_url = entry["request"]["url"]
        entry_ts  = entry.get("startedDateTime", "")

        if "event-stream" in mime or "text/plain" in mime:
            text = _resp_text_raw(entry)
            if text:
                ai_urls = extract_ai_urls_from_sse(text, entry_url, entry_ts, timeline)
                raw_urls.extend(ai_urls)

    # Pass 2: Extract network request URLs
    net_urls = extract_network_urls(entries)
    raw_urls.extend(net_urls)

    # Pass 3: Attribute each URL
    artifacts: List[URLArtifact] = []
    for url_ctx in raw_urls:
        raw = url_ctx.get("url", "")
        if not raw or not raw.startswith("http"):
            continue
        artifact = attribute_url(raw, url_ctx, timeline)
        artifacts.append(artifact)

    # Pass 4: Deduplicate
    artifacts = deduplicate_artifacts(artifacts)

    # Pass 5: Sort — AI first (most forensically interesting), then HUMAN, then PLATFORM
    priority = {"AI": 0, "HUMAN": 1, "PLATFORM": 2}
    artifacts.sort(key=lambda a: (priority.get(a.attribution, 9), -a.confidence))

    # Pass 6: Build relationship map
    chain = build_url_relationship_map(artifacts, timeline)

    return artifacts, timeline, chain
