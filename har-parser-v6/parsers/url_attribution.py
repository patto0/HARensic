def _is_platform_domain(domain: str) -> Tuple[bool, int, str]:
    """
    Returns (is_platform, confidence, reason).
    """
    d = domain.lower()

    if d in _PLATFORM_DOMAINS_EXACT:
        return True, 100, f"Domain '{d}' is a confirmed OpenAI/Anthropic/Google AI platform domain."

    for suffix in _PLATFORM_DOMAIN_SUFFIXES:
        if d.endswith(suffix):
            return True, 95, f"Domain ends with platform infrastructure suffix '{suffix}'."

    # gstatic / google infra
    if d.endswith(".gstatic.com") or d.endswith(".google.com") or d == "www.google.com":
        return True, 90, f"Domain '{d}' is Google CDN/infrastructure (gstatic/google.com)."

    # Datadog and monitoring
    if "datadoghq" in d or "datadog" in d:
        return True, 98, f"Domain '{d}' is Datadog monitoring infrastructure."

    # Analytics
    if "google-analytics" in d or "googletagmanager" in d:
        return True, 95, f"Domain '{d}' is Google Analytics/Tag Manager infrastructure."

    return False, 0, ""


def _classify_platform_path(url: str) -> Tuple[bool, int, str]:
    """Check URL path against known platform patterns."""
    for pattern, description in _PLATFORM_PATH_PATTERNS:
        if pattern.search(url):
            return True, 97, f"URL path matches platform pattern: {description}."
    return False, 0, ""


def _build_platform_evidence(url: str, domain: str, path_desc: str,
                               domain_reason: str, confidence: int) -> str:
    return (
        f"PLATFORM: This URL belongs to AI platform infrastructure. "
        f"{domain_reason or path_desc} "
        f"The AI model does not generate infrastructure requests; users do not "
        f"manually navigate to these endpoints. The platform's systems "
        f"(telemetry, CDN, authentication, anti-bot) make these requests "
        f"automatically as part of service operation. "
        f"Confidence: {confidence}%."
    )


def _build_ai_evidence(url: str, domain: str, url_role: str, source: str,
                        trigger: str, tool_name: str, search_queries: List[str],
                        snippet: str, confidence: int) -> str:
    role_desc = {
        "search_result":      "a raw search index result returned by the AI's search tool",
        "cited_source":       "a citation included in the AI's generated response",
        "retrieved_content":  "a page whose full content was retrieved and read by the AI",
        "supporting_source":  "a supporting reference linked by the AI's cited source",
        "citation_patch_url": "a URL resolved during AI citation patch generation",
        "browsing_result":    "a page returned by the AI's browsing tool",
        "tool_call_url":      "a URL embedded in the AI's tool invocation code",
    }.get(url_role, "an AI-accessed resource")

    query_ctx = ""
    if search_queries:
        query_ctx = f" The model searched for: \"{search_queries[0]}\". "

    trigger_ctx = ""
    if trigger:
        trigger_ctx = f" This URL appeared after the '{trigger}' event in the SSE stream,"
        trigger_ctx += " confirming it was generated during AI tool execution, not user navigation."

    snippet_ctx = ""
    if snippet:
        truncated = snippet[:120] + ("…" if len(snippet) > 120 else "")
        snippet_ctx = f" The AI read content including: \"{truncated}\""

    return (
        f"AI: URL identified as {role_desc}. "
        f"Found inside '{source}' in the SSE event stream.{query_ctx}"
        f"{trigger_ctx}{snippet_ctx} "
        f"The tool '{tool_name or 'SonicBrowserTool'}' accessed this resource on behalf of the model. "
        f"This URL does not appear as a direct browser network request — "
        f"it exists only within the streamed assistant metadata, "
        f"proving server-side AI retrieval. "
        f"Confidence: {confidence}%."
    )


def _build_human_evidence(url: str, domain: str, initiator_type: str,
                           resource_type: str, referer: str, confidence: int) -> str:
    init_ctx = ""
    if initiator_type == "navigation":
        init_ctx = " HAR initiator type is 'navigation', confirming direct browser navigation."
    if resource_type == "document":
        init_ctx += " Resource type is 'document', indicating a top-level page load."
    if referer and "chatgpt.com" not in referer and "openai.com" not in referer:
        init_ctx += f" Referer header shows user navigated from '{referer[:60]}'."

    return (
        f"HUMAN: URL initiated by direct user browser action. "
        f"{init_ctx} "
        f"This request occurred before or independently of any AI tool invocation. "
        f"The domain '{domain}' is not part of platform infrastructure. "
        f"Pattern consistent with manual browsing or user-clicked link. "
        f"Confidence: {confidence}%."
    )


