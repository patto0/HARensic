def extract_network_urls(entries: List[Dict]) -> List[Dict]:
    """
    Extract URLs from direct network requests.
    Classifies initiator type and resource type for HUMAN vs PLATFORM heuristics.
    """
    results = []
    for i, entry in enumerate(entries):
        req    = entry.get("request", {})
        url    = req.get("url", "")
        method = req.get("method", "GET")
        ts     = entry.get("startedDateTime", "")
        # HAR initiator (Chrome DevTools)
        initiator = entry.get("_initiator", {}) or {}
        init_type = initiator.get("type", "")  # "navigation" | "script" | "other"
        resource_type = entry.get("_resourceType", "")  # "document" | "xhr" | "fetch" etc.
        referer   = ""
        for h in req.get("headers", []):
            if h.get("name", "").lower() == "referer":
                referer = h.get("value", "")
                break

        results.append({
            "url":           url,
            "method":        method,
            "entry_index":   i,
            "ts":            ts,
            "initiator_type": init_type,
            "resource_type": resource_type,
            "referer":       referer,
            "source":        "network_request",
        })
    return results


